class Libro:

    titulo = ""
    autor = ""
    genero = ""

    def __init__(self, dtitulo, dautor, dgenero):
        self.titulo = dtitulo
        self.autor = dautor
        self.genero = dgenero
    
    def getTitulo():
        return titulo
    
    def getAutor():
        return autor

    def getGenero():
        return genero